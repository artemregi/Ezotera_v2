    // ── Helper: pick item by index (deterministic) ───────────────────────────
    function pick(arr, seed) {
        return arr[((seed % arr.length) + arr.length) % arr.length];
    }

    // pick from nested array-of-arrays (each element is itself an array)
    function pickGroup(arr, seed) {
        return arr[((seed % arr.length) + arr.length) % arr.length];
    }

    function getZodiacIndex(sign) {
        if (!sign) return 0;
        var idx = ZODIAC_ORDER.indexOf(sign);
        return idx >= 0 ? idx : 0;
    }

    function getZodiacEmoji(sign) {
        return ZODIAC_EMOJIS[sign] || '✨';
    }

    // ── setText helper ────────────────────────────────────────────────────────
    function setText(id, text) {
        var el = document.getElementById(id);
        if (el) el.textContent = text;
    }

    // ── Initialize dashboard ─────────────────────────────────────────────────
    function initDashboard() {
        fetchUserProfile();
        setupEventListeners();
    }

    // ── Fetch user profile from API ──────────────────────────────────────────
    function fetchUserProfile() {
        showLoading();

        fetch('/api/user/profile', {
            method: 'GET',
            credentials: 'include'
        })
        .then(function(response) {
            if (!response.ok) {
                if (response.status === 401) {
                    window.location.href = '/auth/login.html';
                    return;
                }
                var status = response.status;
                return response.json().then(function(errData) {
                    throw new Error((errData && errData.message) || ('Ошибка загрузки профиля [' + status + ']'));
                }).catch(function(parseErr) {
                    if (parseErr.message && parseErr.message.indexOf('[') !== -1) throw parseErr;
                    throw new Error('Ошибка загрузки профиля [' + status + ']');
                });
            }
            return response.json();
        })
        .then(function(result) {
            if (result && result.success) {
                displayUserData(result.user);
            } else {
                showError((result && result.message) || 'Не удалось загрузить данные профиля');
            }
        })
        .catch(function(err) {
            console.error('Profile fetch error:', err);
            showError(err.message || 'Произошла ошибка при загрузке данных');
        });
    }

