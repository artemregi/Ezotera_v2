        var chipZodiac = document.getElementById('chipZodiac');
        if (chipZodiac) {
            if (user.zodiacSign) {
                chipZodiac.textContent = getZodiacEmoji(user.zodiacSign) + ' ' + user.zodiacSign;
            } else {
                chipZodiac.style.display = 'none';
            }
        }

